def AUtoTGCS(MCS):
    """Convert ccl delta coordinates from AU (JA) to TGCS."""
    TGCS = pandas.DataFrame(index=MCS.index, columns=MCS.columns)
    TGCS.loc[:, 'dx'] = MCS.dz
    TGCS.loc[:, 'dy'] = MCS.dx
    TGCS.loc[:, 'dz'] = MCS.dy
    return TGCS


