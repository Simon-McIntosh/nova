def read_clipboard():
    """Read displacment data from clipboard."""
    ccl = pandas.read_clipboard(header=None)
    ccl.set_index(0, inplace=True)
    ccl.index.name = None
    columns = ccl.columns
    ccl.drop(columns=columns[3:], inplace=True)
    ccl.columns = ['dx', 'dy', 'dz']
    return ccl.dropna(0)

